TOPIC_VARIANTS = [
    "Conceptual Foundations of {topic}",
    "Core Skills in {topic}",
    "Worked Examples: {topic}",
    "Error Analysis in {topic}",
    "Real-World Applications of {topic}",
]


def build_topics(chapter_name: str, stage: str, core_topics: List[str], chapter_prereqs: List[str]) -> List[Dict[str, object]]:
    topics: List[Dict[str, object]] = []
    seen_names = set()

    def add_topic(name: str, objective: str, prerequisites: List[str], keywords: List[str], problem_types: List[str]) -> None:
        normalized = name.strip()
        if not normalized or normalized in seen_names:
            return
        seen_names.add(normalized)
        topic_id = f"TOP-{len(topics) + 1:03d}"
        topics.append(
            {
                "topic_id": topic_id,
                "topic_name": normalized,
                "stage": stage,
                "difficulty": "basic" if stage == "basic" else ("intermediate" if stage == "intermediate" else "advanced"),
                "learning_objective": objective,
                "prerequisites": prerequisites,
                "keywords": keywords,
                "problem_types": problem_types,
            }
        )

    for base_topic in core_topics:
        add_topic(
            name=base_topic,
            objective=f"Understand definitions, notation, and core methods for {base_topic.lower()}.",
            prerequisites=chapter_prereqs,
            keywords=[chapter_name, base_topic, stage],
            problem_types=["concept-check", "direct-computation"],
        )
        for template in TOPIC_VARIANTS:
            variant_name = template.format(topic=base_topic)
            add_topic(
                name=variant_name,
                objective=f"Develop fluency and transfer skills in {base_topic.lower()} across varied contexts.",
                prerequisites=[base_topic],
                keywords=[chapter_name, base_topic, "applications"],
                problem_types=["word-problem", "multi-step", "mixed-practice"],
            )

    # Fill up to target topics per chapter with systematic extension topics.
    extension_index = 1
    while len(topics) < TARGET_TOPICS_PER_CHAPTER:
        seed_topic = core_topics[(extension_index - 1) % len(core_topics)]
        add_topic(
            name=f"Advanced Problem Set {extension_index}: {seed_topic}",
            objective=f"Solve challenging and non-routine tasks involving {seed_topic.lower()}.",
            prerequisites=[seed_topic],
            keywords=[chapter_name, seed_topic, "challenge"],
            problem_types=["proof", "challenge", "olympiad-style"],
        )
        extension_index += 1

    return topics[:TARGET_TOPICS_PER_CHAPTER]


def build_curriculum() -> Dict[str, object]:
    chapters: List[Dict[str, object]] = []
    total_topics = 0

    for chapter_index, chapter_data in enumerate(CHAPTER_BLUEPRINTS, start=1):
        chapter_name = str(chapter_data["name"])
        stage = str(chapter_data["stage"])
        description = str(chapter_data["description"])
        prerequisites = list(chapter_data["prerequisites"])
        tags = list(chapter_data["tags"])
        core_topics = list(chapter_data["core_topics"])

        topics = build_topics(
            chapter_name=chapter_name,
            stage=stage,
            core_topics=core_topics,
            chapter_prereqs=prerequisites,
        )

        total_topics += len(topics)

        chapters.append(
            {
                "chapter_id": f"CH-{chapter_index:03d}",
                "chapter_name": chapter_name,
                "stage": stage,
                "chapter_description": description,
                "prerequisites": prerequisites,
                "tags": tags,
                "topic_count": len(topics),
                "topics": topics,
            }
        )

    return {
        "subject_id": "SUB-MATH-001",
        "subject_name": "Mathematics",
        "subject_description": "Comprehensive mathematics curriculum from foundational arithmetic to advanced pure and applied mathematics.",
        "curriculum_version": "1.0",
        "levels": ["basic", "intermediate", "advanced"],
        "total_chapters": len(chapters),
        "total_topics": total_topics,
        "generated_on": date.today().isoformat(),
        "chapters": chapters,
    }


def main() -> None:
    curriculum = build_curriculum()
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with OUTPUT_PATH.open("w", encoding="utf-8") as file:
        json.dump(curriculum, file, indent=2)

    print(f"Created: {OUTPUT_PATH}")
    print(f"Total chapters: {curriculum['total_chapters']}")
    print(f"Total topics: {curriculum['total_topics']}")


if __name__ == "__main__":
    main()
